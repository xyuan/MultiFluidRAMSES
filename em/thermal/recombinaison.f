!     ****************************************
!     *** Calcul des taux de recombinaison ***
!     ****************************************

!     ========================================================
      REAL*8 FUNCTION recomb_dielectronique_maxw(iZ,charge,kT)  ! en cm^3/s
!     ========================================================

!     *********************************************************************************
!     *** Cette routine calcule les taux de recombinaison diélectronique pour tous  ***
!     *** les états d'ionisation des élements compris entre H et Ni en utilisant    ***
!     *** les données de Mazzotta et al., A&AS 133, 403, 1998.                      ***
!     *** Données des ajustements disponibles sur cdsweb.u-strasbg.fr/Abstract.html ***
!     *** Arguments : iZ     -> numéro atomique                                     ***
!     ***             charge -> état d'ionisation                                   ***
!     ***             kT     -> température en eV                                   ***
!     *********************************************************************************

      IMPLICIT REAL*8 (A-H,O-Z)

      ! Variables d'entrée
      INTEGER charge
      REAL*8  kT

      ! Coefficients de l'ajustement :
      COMMON /fit_dielectric/ C(4,28,27),E(4,28,27),A(28,27)

      IF (iZ .GT. 28) STOP 'FUNCTION recombinaison électronique : données manquantes si Z > 28 !'

      IF (charge .EQ. iZ) THEN ! Pas de recombinaison à deux électrons sur un atome complètement ionisé
        recomb_dielectronique_maxw = 0.D0
        RETURN
      ENDIF  

      somme = 0.D0
      DO i = 1, 4
        somme = somme + C(i,iZ,charge)*DEXP(-E(i,iZ,charge)/kT)
      END DO

      recomb_dielectronique_maxw = A(iZ,charge)*somme/kT**1.5D0

      RETURN
      END FUNCTION recomb_dielectronique_maxw

!     ===================================================
      REAL*8 FUNCTION recomb_radiative_maxw(iZ,charge,kT)  ! en cm^3/s
!     ===================================================

!     ********************************************************************************************
!     *** Cette routine calcule les taux de recombinaison radiative pour tous les états        ***
!     *** d'ionisation des élements compris entre H et Zn en utilisant les données suivantes : ***
!     *** H-like, He-like, Li-like, Na-like : Verner & Ferland, 1996, ApJS 103, 467.
!     *** Other ions of C, N, O, Ne : Pequignot et al., 1991, A&A 251, 680.
!     ***                             refitted by Verner & Ferland formula to ensure correct asymptotes.
!     *** Other ions of Mg, Si, S, Ar, Ca, Fe, Ni : Shull & Van Steenberg, 1982, ApJS 48, 95.
!     *** Other ions of Na, Al: Landini & Monsignori Fossi, 1990, A&AS, 82, 229.
!     *** Other ions of F, P, Cl, K, Ti, Cr, Mn, Co : Landini & Monsignori Fossi, 1991, A&AS 91, 183.
!     ***       (excluding Ti I-II, Cr I-IV, Mn I-V, Co I)
!     *** Fe XIV-XV and Fe XVII-XXIII : Arnaud & Raymond, 1992, ApJ 398, 394.
!     *** All other species : interpolations of the power-law fits
!     *** Arguments : iZ     -> numéro atomique (≤ 30)
!     ***             charge -> état d'ionisation avant recombinaison
!     ***             kT     -> température en eV
!     ********************************************************************************************

      IMPLICIT REAL*8 (A-H,O-Z)
      INCLUDE 'include.f'

      ! Variables d'entrée
      INTEGER charge
      REAL*8  kT
      
      ! Coefficients de l'ajustement :
      COMMON /fit_dielectric/ C(4,28,27),E(4,28,27),A(28,27)

      COMMON /rrec/ rrec(2,30,30)
      COMMON /rnew/ rnew(4,30,30)
      COMMON /fer/ fe(3,13)

      IF ((charge .LT. 1) .OR. (charge .GT. iZ)) STOP 'FUNCTION recombinaison_radiative : la variable <charge> est incorrecte !'

      iNe = (iZ - charge) + 1 ! Nombre d'électrons restants sur l'ion recombiné
      T   = eV_to_kelvin * kT ! Température en Kelvins

      IF ( (iNe .LE.  3)                  .OR.
     &     (iNe .EQ. 11)                  .OR.
     &    ((iZ .GE. 6) .AND. (iZ .LE. 8)) .OR.
     &     (iZ .EQ. 10) )                       THEN

         x = DSQRT(T/rnew(3,iZ,iNe))
         taux = rnew(1,iZ,iNe)/(x * (1.D0 + x)**(1.D0 - rnew(2,iZ,iNe)) 
     &        * (1.D0 + DSQRT(T/rnew(4,iZ,iNe)))**(1.D0+rnew(2,iZ,iNe)))

      ELSE

         T4 = 1.0D-04*T
         IF ((iZ .EQ. 26) .AND. (iNe .LE. 13)) THEN
            taux = fe(1,iNe)/T4**(fe(2,iNe) + fe(3,iNe)*DLOG10(T4))
         ELSE
            taux = rrec(1,iZ,iNe)/T4**rrec(2,iZ,iNe)
         END IF

      END IF
      recomb_radiative_maxw = taux

      RETURN
      END FUNCTION recomb_radiative_maxw

